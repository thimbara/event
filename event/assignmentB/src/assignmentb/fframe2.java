    private void getData() {
        
         try {
            Connection connector = DriverManager.getConnection("jdbc:mysql://localhost:3306/data","root","");
            PreparedStatement statement = connector.prepareStatement("SELECT * FROM tableC");
            ResultSet result = statement.executeQuery();
            
            //DefaultListModel model = new DefaultListModel();
            Vector<String> dataVector = new Vector<>();
            while (result.next()){
                
                String name = result.getString("name");
                int mobile = result.getInt("mobile");
                String gender = result.getString("gender");
                String dob = result.getString("dob");
                String address = result.getString("address");
                dataVector.add(" Name: " + name + " |Mobile: " + mobile + " |Gender: " + gender + " |D.O.B: " + dob + " |Addrress: " + address);
                        
            }
            jList1.setListData(dataVector);
            result.close();
            statement.close();
            connector.close();
        } catch (Exception ex) {
            System.out.println(ex);

    }}
