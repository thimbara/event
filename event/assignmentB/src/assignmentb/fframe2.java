    private String getDob() {
        String date = (String)jComboBox1.getSelectedItem();
        String month = (String)jComboBox2.getSelectedItem();
        String year = (String)jComboBox3.getSelectedItem();
        String dob = date + "-" + month + "-" +  year;
        
        return dob;
    }

    private String getGender() {
        String gender = "";
        if (jRadioButton1.isSelected()) {
            gender = jRadioButton1.getText();
        } else if (jRadioButton2.isSelected()) {
            gender = jRadioButton2.getText();
        } else {
            String g = "Please select your gender";
            JOptionPane.showMessageDialog(rootPane, g, "Warning!!", JOptionPane.PLAIN_MESSAGE);
        }
        return gender;
    }

